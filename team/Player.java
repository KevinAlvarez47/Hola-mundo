
package team;

public class Player {
String name;
int goals;

     Player(String name,int goals) {
      this.name=name;
      this.goals=goals;
     }
   public String getName()
   {
   return this.name;
   }
   public int getGoals()
   {
   return goals;
   }

    public String getPlayerName() {
       return name;
    }
}
